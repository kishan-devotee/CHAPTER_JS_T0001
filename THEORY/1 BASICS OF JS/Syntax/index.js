// Syntax of Javascript

let a; // Declaration of a variable

a = 10; // Initialization of a variable

console.log(a); // Output: 10

a = 20; // Re-initialization of a variable

console.log(a); // Output: 20

const b = 30; // Declaration and initialization of a constant

console.log(b); // Output: 30

b = 40; // Trying to re-initialize a constant results in a TypeError

console.log(b); // Output: 30


